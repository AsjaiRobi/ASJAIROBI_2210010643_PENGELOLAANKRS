    private void loadComboBoxNPM() {
        try (Connection conn = getConnection()) {
            String sql = "SELECT npm FROM mahasiswa";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            NPM.removeAllItems(); // Hapus item lama
            while (rs.next()) {
                NPM.addItem(rs.getString("npm"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat NPM: " + e.getMessage());
        }
    }

    private void loadMahasiswaTable() {
        try (Connection conn = getConnection()) {
            String sql = "SELECT * FROM mahasiswa";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            DefaultTableModel model = (DefaultTableModel) MahasiswaTabel.getModel();
            model.setRowCount(0); // Kosongkan tabel sebelum memuat ulang

            while (rs.next()) {
                Object[] row = {
                    rs.getString("npm"),
                    rs.getString("nama"),
                    rs.getString("tanggal_lahir"),
                    rs.getString("jurusan"),
                    rs.getString("no_telepon"),
                    rs.getString("email")
                };
                model.addRow(row);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data mahasiswa: " + e.getMessage());
        }
    }

    private void loadKRSTable() {
        try (Connection conn = getConnection()) {
            String sql = "SELECT * FROM pengambilan_krs";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            DefaultTableModel model = (DefaultTableModel) KRS.getModel();
            model.setRowCount(0); // Kosongkan tabel sebelum memuat ulang

            while (rs.next()) {
                Object[] row = {
                    rs.getInt("id_pengambilan"),
                    rs.getString("npm"),
                    rs.getString("kode_mata_kuliah"),
                    rs.getString("nama_mata_kuliah"),
                    rs.getInt("sks"),
                    rs.getString("semester"),
                    rs.getInt("tahun_akademik"),
                    rs.getString("nama_dosen")
                };
                model.addRow(row);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data KRS: " + e.getMessage());
        }
    }

