    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
    String npm = txtENPEEM.getText();
    String nama = txtNama.getText();
    String tanggalLahir = txtTANGGAL.getText();
    String jurusan = JURUSAN.getText();
    String noTelepon = NoTelepon.getText();
    String email = Email.getText();

    // Validasi input
    if (npm.isEmpty() || nama.isEmpty() || tanggalLahir.isEmpty() || jurusan.isEmpty() || noTelepon.isEmpty() || email.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Harap lengkapi semua data!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    } else {
        // Masukkan data ke database
        String sql = "INSERT INTO mahasiswa (npm, nama, tanggal_lahir, jurusan, no_telepon, email) VALUES (?, ?, ?, ?, ?, ?)";
        try (PreparedStatement stmt = getConnection().prepareStatement(sql)) {
            stmt.setString(1, npm);
            stmt.setString(2, nama);
            stmt.setString(3, tanggalLahir);
            stmt.setString(4, jurusan);
            stmt.setString(5, noTelepon);
            stmt.setString(6, email);

            stmt.executeUpdate();
            loadMahasiswaTable();
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan ke database.", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menambahkan data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if (txtENPEEM.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "NPM tidak boleh kosong untuk melakukan update!");
            return;
        }

        try (Connection conn = getConnection()) {
            // SQL untuk update data mahasiswa berdasarkan NPM
            String sql = "UPDATE mahasiswa SET nama = ?, tanggal_lahir = ?, jurusan = ?, no_telepon = ?, email = ? WHERE npm = ?";
            PreparedStatement pst = conn.prepareStatement(sql);

            // Mengatur parameter update
            pst.setString(1, txtNama.getText());
            pst.setString(2, txtTANGGAL.getText());
            pst.setString(3, JURUSAN.getText());
            pst.setString(4, NoTelepon.getText());
            pst.setString(5, Email.getText());
            pst.setString(6, txtENPEEM.getText());

            // Menjalankan update
            int rowsUpdated = pst.executeUpdate();
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Data mahasiswa berhasil diupdate!");
                loadMahasiswaTable(); // Memuat ulang data tabel mahasiswa
                clearInputMahasiswa(); // Mengosongkan input
            } else {
                JOptionPane.showMessageDialog(null, "Data mahasiswa dengan NPM tersebut tidak ditemukan.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error btnEditMahasiswa: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed


